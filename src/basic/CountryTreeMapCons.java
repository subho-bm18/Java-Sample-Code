package basic;

public class CountryTreeMapCons {
	
	 
		String name;  
		long population;  
	 
		public CountryTreeMapCons(String name, long population) {  
			super();  
			this.name = name;  
			this.population = population;  
		}  
		public String getName() {  
			return name;  
		}  
		public void setName(String name) {  
			this.name = name;  
		}  
		public long getPopulation() {  
			return population;  
		}  
		public void setPopulation(long population) {  
			this.population = population;  
		}  
	 
	}

